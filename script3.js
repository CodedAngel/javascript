// Array Methods
//Push

let array = [ "a","b","c","d","e","f"];
array.push("g",1,"z");
console.log(array);
//Pop
// pop removes an item from the end of the array
array =[ "a","b","c","d","e","f"];
array.pop();
array.pop();
console.log(array);

//shift
// shift removes an item from the start of the array
array =[ "a","b","c","d","e","f"];
array.shift();
console.log(array);
//unshift
// unshift adds items to the start of an array
 array =[ "a","b","c","d","e","f"];
array.unshift("g",1,"z");
console.log(array);
